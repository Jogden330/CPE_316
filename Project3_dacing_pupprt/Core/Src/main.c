     	     	  TIM4->CCR2 +=  (freq);              //set channel one compare value
     	     	  break;
     	case(6):
     			  state = Monkey;
     	     	  TIM4->CCR2 +=  (freq);              //set channel one compare value
     	     	  break;
     	case(7):
     			  state = Shimmy;
